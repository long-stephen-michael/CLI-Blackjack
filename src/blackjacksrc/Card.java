package blackjacksrc;

public class Card{
	
	public static class card{
		
		public blackjacksrc.Rank.rank rank;
		public blackjacksrc.Suit.suit suit;
	
		public card() {
			this.rank = rank;
			this.suit = suit;
		}


		public blackjacksrc.Rank.rank getRank() {
			return rank;
		}
		
		public blackjacksrc.Suit.suit getSuit() {
			return suit;
		}
		
	}

	}
	